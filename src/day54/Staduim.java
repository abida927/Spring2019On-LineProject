package day54;

public  class Staduim {
	
	int capacity;
	String food;
	
	
	public Staduim(int capacity, String food) {
		super();
		this.capacity = capacity;
		this.food = food;
	}
	
	@Override
	public String toString() {
		return "Staduim [capacity=" + capacity + ", food=" + food + "]";
	}
	
	
	

}
